/**
 * 获取中文字符的拼音首字母，非中文字符返回原字符。
 * 未识别的汉字返回空字符串（不参与匹配）。
 */
function charToPinyin(c: string): string {
  if (!/[一-鿿]/.test(c)) return c.toLowerCase();
  for (const chars of Object.keys(PINYIN_MAP)) {
    if (chars.includes(c)) return PINYIN_MAP[chars];
  }
  return ""; // 未识别汉字
}

/** 提取字符串的拼音首字母序列（仅中文部分） */
export function toPinyinInitials(text: string): string {
  let result = "";
  for (const c of text) {
    result += charToPinyin(c);
  }
  return result;
}
